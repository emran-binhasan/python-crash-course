x = 10
y = 2
result1 = x + y # Addition
result2 = x - y #Subtraction
result3 = x * y # Multiplication
result4 = x / y #Division always returns a float in Python
print(result1, result2, result3, result4)  # Print the results of arithmetic operations


result5 = x**y  # Exponentiation
result6 = x // y  # Floor division
result7 = x % y  # Modulus
print(result5, result6, result7)  # Print the results of exponentiation, floor division, and modulus