hello = 'hello'
print(hello.upper())  # Convert to uppercase
print(hello.lower())  # Convert to lowercase
print(hello.title())  # Convert to title case
print(hello.capitalize())  # Capitalize the first letter
print(hello.replace('e', 'a'))  # Replace 'e' with 'a'
print(hello.find('l'))  # Find the first occurrence of 'l'
print(hello.count('l'))  # Count occurrences of 'l'
print(hello.startswith('he'))  # Check if it starts with 'he'